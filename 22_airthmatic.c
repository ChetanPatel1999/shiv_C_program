// example of airthmatic operator
#include <stdio.h>
void main()
{
    int a, b, c;
    a = 12;
    b = 7;
    c = a + b;
    printf("addition = %d\n", c);

    c = a - b;
    printf("subtraction = %d\n", c);

    c = a * b;
    printf("multiplication = %d\n", c);

    c = a / b;
    printf("division = %d\n", c);

    c = a % b;
    printf("reminder = %d\n", c);
}