#include <stdio.h>
void main()
{
    int a;

    char b;

    float c;

    double d;

    printf("size of int = %d\n", sizeof(a)); // sizeof() operator return size of variable
    printf("size of char = %d\n", sizeof(b));
    printf("size of float = %d\n", sizeof(c));
    printf("size of double = %d\n", sizeof(d));
}